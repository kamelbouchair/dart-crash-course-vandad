void main(List<String> args) {
  const age1 = 10;
  const age2 = 20;
  print(age1 + age2);
  print(age1 + age2 + 30);
  print(age1 * age2);
  print(age2 - age1);
  const double devided1 = age2 / age1;
  print(devided1);
  const devided2 = age2 ~/ age1; // the var set the value to 'int' by default
  print(devided2);
}
